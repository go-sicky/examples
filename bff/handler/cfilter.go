package handler

import "github.com/gofiber/fiber/v2"

type CFilter struct {
}

func NewCFilter() *CFilter {
	return new(CFilter)
}

func (h *CFilter) Register(app *fiber.App) {
	app.Post("/filter", h.filter).Name("POST.filter")
}

func (h *CFilter) filter(ctx *fiber.Ctx) error {
	return ctx.JSON(nil)
}

